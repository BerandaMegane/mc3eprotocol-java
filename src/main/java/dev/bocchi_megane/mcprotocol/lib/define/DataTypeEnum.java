package dev.bocchi_megane.mcprotocol.lib.define;

/**
 * データ型列挙型
 * PLCデバイスのデータサイズを定義します。
 * MC3Eプロトコルで使用される各種デバイスのデータ幅を表します。
 */
public enum DataTypeEnum {
    /** 
     * ビット型（1ビット）
     * リレー系デバイス（X, Y, M, L, F, V, B, S等）で使用される1ビットデータ
     * ON/OFFの状態を表し、論理値として扱われます
     */
    BIT,
    
    /** 
     * ワード型（16ビット）
     * レジスター系デバイス（D, W, R, ZR等）やタイマー・カウンター現在値で使用される16ビットデータ
     * 数値データや文字データの格納に使用され、-32,768～32,767の範囲を扱えます
     */
    WORD,
    
    /** 
     * ダブルワード型（32ビット）
     * ロングタイマー現在値（LTN）やロングカウンター現在値（LCN）等で使用される32ビットデータ
     * より大きな数値範囲（-2,147,483,648～2,147,483,647）を扱う場合に使用されます
     */
    DWORD,
}
